""" Backslash \ 
Donde se va usar para :
- Escapar de comillas
- Saltos de linea 
- Tabulaciones 
 """

print("Ejemplo 1 : Imprimir doble comillas")
mensaje = "Ella dijo \"hola\" al entrar"
print(mensaje)

print("Ejemplo 2 : Salto de linea")
print("hola \nmundo")

print("Ejemplo 3 : Tabulacion")
print("Nombre:\tJuan")
print("Apellido:\tPerez")

print("Ejemplo 4 : Imprimir doble comillas")
print("El libro se llama \"1984\"")

print("Ejemplo 5 : Comillas simples")
print('Jose O\'Gorman')

